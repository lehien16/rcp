    SELECT COALESCE(SUM(cthd_ve.GiaVe), 0) + COALESCE(SUM(cthd_dv.ThanhTien), 0)
    FROM CTHDVe cthd_ve 
    LEFT JOIN CTHDDichVu cthd_dv ON cthd_ve.MaHD = cthd_dv.MaHD
    WHERE cthd_ve.MaHD = hd.MaHD
    GROUP BY cthd_ve.MaHD
);

-- Cập nhật cho các hóa đơn chỉ có dịch vụ (không có vé)
UPDATE HoaDon hd
SET hd.TongTien = (
    SELECT COALESCE(SUM(cthd_dv.ThanhTien), 0)
    FROM CTHDDichVu cthd_dv
    WHERE cthd_dv.MaHD = hd.MaHD
    GROUP BY cthd_dv.MaHD
)
WHERE hd.MaHD NOT IN (SELECT DISTINCT MaHD FROM CTHDVe);

